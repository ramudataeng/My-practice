# Find all employees with a salary greater than 80,000.
SELECT name, salary 
FROM employees
WHERE salary >= 80000;

#List employees who joined before 2020-01-01.
SELECT name, hire_date
FROM employees 
WHERE hire_date < '2020-01-01';

# Get all orders where the amount is greater than 1000.
SELECT order_id, product, amount
FROM orders
WHERE amount > 1000;

# Show employees who belong to the IT department.
SELECT employee_id, name, department
FROM employees
WHERE department = 'IT';

# Retrieve orders placed after 2023-02-01.
SELECT order_id, product
FROM orders
WHERE order_date > '2023-02-01';

# Find the total number of employees in each department.
SELECT department, COUNT(*) AS total_employees
FROM employees
GROUP BY department;

# Calculate the average salary per department.
SELECT department, AVG(salary)
FROM employees
GROUP BY department;

# Find the total order amount for each employee.
SELECT employee_id, SUM(amount) AS total_spent
FROM orders
GROUP BY employee_id;

# Find the number of orders placed for each product.
SELECT product, COUNT(*) AS quantity
FROM orders
GROUP BY product;

# Find the total revenue generated by all orders combined.
SELECT SUM(amount) AS revenue
FROM orders;

SELECT * FROM employees, orders;

# Show all employees along with their orders (include employees with no orders).
SELECT e.name, o.product
FROM employees e
LEFT JOIN orders o ON e.employee_id = o.employee_id;

# Find employees who have placed at least one order.
SELECT DISTINCT e.*
FROM employees e
JOIN orders o ON e.employee_id = o.employee_id;

# Show employee name and total order amount per employee.
SELECT e.name, SUM(o.amount) AS total_amount
FROM employees e
LEFT JOIN orders o ON e.employee_id = o.employee_id
GROUP BY e.name;

# Find departments along with total order revenue generated by each department.
SELECT e.department, SUM(o.amount) AS revenue
FROM employees e
LEFT JOIN orders o ON e.employee_id = o.employee_id
GROUP BY e.department;

# List all orders with employee name and department.
SELECT e.name, e.department, o.*
FROM employees e
RIGHT JOIN orders o ON e.employee_id = o.employee_id;

SELECT e.name, e.department, o.product, o.amount
FROM employees e
RIGHT JOIN orders o ON e.employee_id = o.employee_id;

SELECT * FROM employees, orders;
# Find employees who have never placed an order.
SELECT e.*
FROM employees e
LEFT JOIN orders o ON e.employee_id = o.employee_id
WHERE o.order_id is NULL;

# Find the employee who generated the highest total order amount.
SELECT e.name, SUM(o.amount) AS total_amount
FROM employees e 
LEFT JOIN orders o ON e.employee_id = o.employee_id
GROUP BY e.name
ORDER BY total_amount DESC
LIMIT 1;

# Find the product that generated the highest revenue.
SELECT product, SUM(amount) AS revenue
FROM orders 
GROUP BY product
ORDER BY revenue DESC
LIMIT 1;

# Find departments where the average salary is greater than 80,000.
SELECT department, AVG(salary) AS avg_salary
FROM employees
GROUP BY department
HAVING avg_salary > 80000;


# Find employees who have placed more than 2 orders.
SELECT e.name, COUNT(o.product) AS no_orders
FROM employees e
JOIN orders o ON e.employee_id = o.employee_id
GROUP BY e.name
HAVING no_orders >= 2;

